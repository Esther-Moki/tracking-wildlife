package models;

public class Ranger {
    private String name;
    private String location;

    public Ranger(String name, String location) {
        this.name = name;
        this.location = location;
    }

    public Ranger() {
    }

    public String getName() {
        return name;
    }

    public String getLocation() {
        return location;
    }

}

